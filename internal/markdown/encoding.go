package markdown

import (
	"fmt"
	"unicode/utf8"
)

type Encoding string

const (
	EncodingUTF8    Encoding = "utf-8"
	EncodingGBK     Encoding = "gbk"
	EncodingShiftJIS Encoding = "shift-jis"
)

func isLikelyUTF8(raw []byte) bool {
	return utf8.Valid(raw)
}

func tryDecodeGBK(raw []byte) ([]byte, bool) {
	result := make([]byte, 0, len(raw))
	i := 0
	for i < len(raw) {
		b := raw[i]
		if b <= 0x7F {
			result = append(result, b)
			i++
			continue
		}
		if b >= 0x81 && b <= 0xFE && i+1 < len(raw) {
			next := raw[i+1]
			if (next >= 0x40 && next <= 0x7E) || (next >= 0x80 && next <= 0xFE) {
				r := decodeGBK(b, next)
				result = append(result, []byte(string(r))...)
				i += 2
				continue
			}
		}
		return nil, false
	}
	return result, true
}

func tryDecodeShiftJIS(raw []byte) ([]byte, bool) {
	result := make([]byte, 0, len(raw))
	i := 0
	for i < len(raw) {
		b := raw[i]
		if b <= 0x7F {
			result = append(result, b)
			i++
			continue
		}
		if ((b >= 0x81 && b <= 0x9F) || (b >= 0xE0 && b <= 0xEF)) && i+1 < len(raw) {
			next := raw[i+1]
			if (next >= 0x40 && next <= 0x7E) || (next >= 0x80 && next <= 0xFC) {
				r := decodeShiftJIS(b, next)
				result = append(result, []byte(string(r))...)
				i += 2
				continue
			}
		}
		return nil, false
	}
	return result, true
}

func decodeGBK(hi, lo byte) rune {
	cp := gbkCodepoint(hi, lo)
	if cp == 0 {
		return rune(hi)<<8 | rune(lo)
	}
	return cp
}

func decodeShiftJIS(hi, lo byte) rune {
	cp := shiftJISCodepoint(hi, lo)
	if cp == 0 {
		return rune(hi)<<8 | rune(lo)
	}
	return cp
}


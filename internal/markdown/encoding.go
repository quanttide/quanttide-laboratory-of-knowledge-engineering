func DetectAndDecode(raw []byte) ([]byte, Encoding, error) {
	if len(raw) == 0 {
		return nil, EncodingUTF8, fmt.Errorf("%w: empty input", ErrEmptyFile)
	}
	if isLikelyUTF8(raw) {
		return raw, EncodingUTF8, nil
	}
	if decoded, ok := tryDecodeGBK(raw); ok {
		return decoded, EncodingGBK, nil
	}
	if decoded, ok := tryDecodeShiftJIS(raw); ok {
		return decoded, EncodingShiftJIS, nil
	}
	return nil, EncodingUTF8, fmt.Errorf("%w: not UTF-8, GBK, or Shift-JIS", ErrUnknownEncode)
}
